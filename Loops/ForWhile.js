// while - repete enquanto a condição for verdadeira
let i = 0;
while (i < 5) {
  console.log(i);
  i++;
}

console.log(`---------------------`);

// for - versão mais compacta

for (let jao = 0; jao < 5; jao++) {
  console.log(jao);
}

